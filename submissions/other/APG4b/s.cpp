#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;

  // 以下のコメント/* */を消して追記する
  cout << seconds /* 1年は何秒か */ << endl;
  cout << seconds*2/* 2年は何秒か */ << endl;
  cout << seconds*5/* 5年は何秒か */ << endl;
  cout << seconds*10/* 10年は何秒か */ << endl;
}
